package mx.com.clickapuntos.bean;

public class PremiosBean {
	private String nombreCliente="";
	private String descPremio="";
	private String imgCliente="";
	public String getNombreCliente() {
		return nombreCliente;
	}
	public void setNombreCliente(String nombreCliente) {
		this.nombreCliente = nombreCliente;
	}
	public String getDescPremio() {
		return descPremio;
	}
	public void setDescPremio(String descPremio) {
		this.descPremio = descPremio;
	}
	public String getImgCliente() {
		return imgCliente;
	}
	public void setImgCliente(String imgCliente) {
		this.imgCliente = imgCliente;
	}
}
